    private void btnNhapDiemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnNhapDiemMouseClicked
        Pattern pt = Pattern.compile(kp.getMaSinhVien());
        Matcher m = pt.matcher(txtMaSinhVien.getText().trim());

        try {
            if (txtMaSinhVien.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Mã sinh viên không được để trống!");
                return;
            }
            if (!m.find()) {
                JOptionPane.showMessageDialog(rootPane, "Mã sinh viên bắt đầu bằng SV và theo sau 2 số!");
                return;
            }
            if (txtDiemThy.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Điểm thi không dược để trống!");
                return;
            }
            
            double diemthi = Double.parseDouble(txtDiemThy.getText());

            if (diemthi < 0 || diemthi > 10) {
                JOptionPane.showMessageDialog(rootPane, "Điểm thi phai trong khoảng tư 0 đến 10!");
                return;
            }
            try {
                Ketqua kq = getdiem();
                boolean tb = db.updatediemthi(kq);
                if (tb) {
                    JOptionPane.showMessageDialog(rootPane, "Thông báo thành công");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Thông báo không thành công");
                }
                loadTable();

            } catch (HeadlessException ex) {
                System.out.println("lỗi = " + ex.toString());
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(rootPane, "Điểm thi phải là số!");
            return;
        }
    }//GEN-LAST:event_btnNhapDiemMouseClicked

