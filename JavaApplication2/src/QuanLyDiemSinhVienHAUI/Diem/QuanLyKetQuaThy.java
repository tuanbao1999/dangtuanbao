    private Ketqua getdiem() {
        String mamon = cbmamon.getSelectedItem().toString();
        String masv = txtMaSinhVien.getText().trim();
        Double diemthy = Double.parseDouble(txtDiemThy.getText().trim());
        return new Ketqua(mamon, null, null, -0.5, masv, null, -0.5, -0.5, -0.5, Diemtb, -0.5, null, diemthy, -0.5, -0.5, null, null);
    }

    public ArrayList<Ketqua> getdsv(ResultSet rs) {
        ArrayList<Ketqua> items = new ArrayList<Ketqua>();

        try {
            while (rs.next()) {
                Ketqua item = new Ketqua();
                item.setMaMon(rs.getString(1));
                item.setTenMon(rs.getString(2));
                item.setSoTinChi(rs.getDouble(3));
                item.setHocKy(rs.getDouble(4));
                item.setMaSinhVien(rs.getString(5));
                item.setHoTen(rs.getString(6));
                item.setDiemTX1(rs.getDouble(7));
                item.setDiemTX2(rs.getDouble(8));
                item.setDiemGiuaKy(rs.getDouble(9));
                item.setDiemTrungBinh(rs.getDouble(10));
                item.setSoTietNghi(rs.getDouble(11));
                item.setDieuKienDuThi(rs.getString(12));
                if (rs.getString(13).equals("0.0")) {

                    item.setDiemthy(null);
                    item.setDiemTongKetDiem10(null);
                    item.setDiemTongketDiem4(null);
                } else {
                    item.setDiemthy(rs.getDouble(13));
                    item.setDiemTongKetDiem10(rs.getDouble(14));
                    item.setDiemTongketDiem4(rs.getDouble(15));
                }
                if (rs.getString(12).equals("Học lại")) {
                    item.setDiemChu("F");
                    item.setXepLoai("Kém");
                } else {
                    item.setDiemChu(rs.getString(16));
                    item.setXepLoai(rs.getString(17));
                }

                items.add(item);
                txtSoTinChi.setText(rs.getString(3));
                txtTenMon.setText(rs.getString(2));
                txtHocKy.setText(rs.getString(4));
                Diemtb = rs.getDouble(10);

            }

        } catch (SQLException ex) {

        }
        return items;
    }

