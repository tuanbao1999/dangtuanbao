    private taikhoan getThiSinhDetail() {
        String taikhoan = txttaikhoan.getText().trim();
        String matkhau = jpwmatkhau.getText().trim();
        return new taikhoan(taikhoan,matkhau,"SinhVien",null,null,null,null,null);
    }
    public static taikhoan getTaiKhoanObject(ResultSet rs){
        taikhoan item = new taikhoan();
        
        try {
            if(rs.next()){
                item.setTentaikhoan(rs.getString(1));
                item.setMatkhau(rs.getString(2));
                item.setQuyentruycap(rs.getString(3));
                item.setMasv(rs.getString(4));
            }else{
                item = null;
            }
        } catch (SQLException ex) {
            
        }
        
        return item;
    }
