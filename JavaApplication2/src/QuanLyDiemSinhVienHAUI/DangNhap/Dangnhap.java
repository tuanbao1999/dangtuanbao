    private void btndangnhapMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btndangnhapMouseClicked
        // TODO add your handling code here:
        taikhoan tk = getThiSinhDetail();
        try{
            taikhoan dn=new taikhoan();
            dn= getTaiKhoanObject(db.getTaiKhoan(tk));
            if(dn != null)
            {
                String selecteditem =(String) cbquyentruycap.getSelectedItem();
                if(dn.getQuyentruycap().equals(selecteditem)){
                        setmasv(dn.getMasv());
                        setquyentruycap(dn.getQuyentruycap());
                        settkk(txttaikhoan.getText());
                    if(selecteditem.equals("SinhVien"))
                    {
                        sinhvien sv=new sinhvien();
                        sv.setVisible(true);
                        this.setVisible(false); 
                    }
                    else
                    {
                       
                        Main m=new Main();
                        m.setVisible(true);
                        this.setVisible(false); 
                    }
                    
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane, "tài khoản mật khẩu không chính xác");
                }
            }
            else
            {
                 JOptionPane.showMessageDialog(rootPane, "tài khoản mật khẩu không chính xác");
            }
        }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "đăng nhập thất bại " + ex.toString());
        }
    }//GEN-LAST:event_btndangnhapMouseClicked

