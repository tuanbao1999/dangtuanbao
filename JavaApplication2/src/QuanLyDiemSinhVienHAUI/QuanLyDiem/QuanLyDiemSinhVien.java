    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        try {
            //lấy dòng chọn trong table
            int row=jTableDiemSinhVien.getSelectedRow();
            //lấy dữ liệu từ giao diện tạo thí sinh mới
            String  soBD=jTableDiemSinhVien.getValueAt(row, 0).toString();          
            //lấy thí sinh từ giao diện làm thành thí sinh mới
            DiemSinhVien newTS=getThiSinhDetail();
            if(diemsinhvien.SuaThongTinSV(soBD, newTS))
                System.out.println("Sửa thành công");
            else
                System.out.println("Không sửa được!");
            //lấy lại dữ liệu cho bảng
             loadTable();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Không sửa được "+ ex.toString() );
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        try {
            int click=JOptionPane.showConfirmDialog(null,"Chắc chắn xóa điểm sinh viên này","Xoa?",JOptionPane.YES_NO_OPTION);
            if (click==JOptionPane.YES_OPTION) {
                //lấy dòng chọn hiện thời trong table
                int row=jTableDiemSinhVien.getSelectedRow();
                //lấy số báo danh ở cột 0
                String  soBD=jTableDiemSinhVien.getValueAt(row, 0).toString();
                //truyền vào phognf thi xóa theo sbd
                diemsinhvien.XoaSinhVien(soBD);
                clearTextDetail();
                loadTable();}
        } catch (Exception ex) {            
            JOptionPane.showMessageDialog(null, "Không xóa được "+ ex.toString() );
        }
    }//GEN-LAST:event_btnXoaActionPerformed

