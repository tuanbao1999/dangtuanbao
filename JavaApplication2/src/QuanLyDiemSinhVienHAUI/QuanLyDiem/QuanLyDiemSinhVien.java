    private void setTextDetail(DiemSinhVien ts){
        txtMaSinhVien.setText(ts.getMaSinhVien());
        txtHoTen.setText(ts.getHoTen());
        cbLop.setSelectedItem(ts.getMaLop());
        cbMonHoc.setSelectedItem(ts.getMaMon());
        txtDiemTrungBinh.setText(String.valueOf(ts.getDiemTB()));
        txtDiemThy.setText(String.valueOf(ts.getDiemThy()));
        txtDiemTongKet.setText(String.valueOf(ts.getDiemTongKet()));
        cbHanhKiem.setSelectedItem(ts.getHanhKiem());
        cbHocKy.setSelectedItem(ts.getHocKy());
        txtGhiChu.setText(ts.getGhiChu());
    }

    private DiemSinhVien getThiSinhDetail(){
        String masv=txtMaSinhVien.getText().trim();
        String hoTen=txtHoTen.getText().trim();
        String maLop = cbLop.getSelectedItem().toString();
        String monHoc = cbMonHoc.getSelectedItem().toString();
        float diemTB = Float.parseFloat(txtDiemTrungBinh.getText()); 
        float diemThy = Float.parseFloat(txtDiemThy.getText()); 
        float diemTongKet =((diemTB + diemThy*2)/3);
        String hanhKiem = cbHanhKiem.getSelectedItem().toString();
        String hocKy = cbHocKy.getSelectedItem().toString();
        String ghiChu = txtGhiChu.getText().trim();
        return new DiemSinhVien(masv, hoTen, maLop, monHoc, diemTB, diemThy, diemTongKet, hanhKiem, hocKy, ghiChu);
        
    }
