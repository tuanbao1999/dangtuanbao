    public void clearTextDetail() {
        txtMaMon.setText("");
        txtTenMon.setText("");
        txtSoTinChi.setText("");
        txtMaGiaoVien.setText("");
        txtHocKy.setText("");
    }

    private void setTextDetail(MonHoc ts) {
        txtMaMon.setText(ts.getMaMon());
        txtTenMon.setText(ts.getTenMon());
        txtSoTinChi.setText(String.valueOf(ts.getSoTC()));
        txtMaGiaoVien.setText(ts.getMaGV());
        txtHocKy.setText(ts.getHocKy());
        cbMaKhoa.setSelectedItem(ts.getMaKhoa());

    }

    private MonHoc getThiSinhDetail() {
        String mamh = txtMaMon.getText().trim();
        String tenMon = txtTenMon.getText().trim();
        int soTC = Integer.parseInt(txtSoTinChi.getText());
        String MaGV = txtMaGiaoVien.getText().trim();
        String hocKy = txtHocKy.getText().trim();
        String maKhoa = cbMaKhoa.getSelectedItem().toString();
        return new MonHoc(mamh, tenMon, soTC, MaGV, hocKy, maKhoa);
    }

