    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        // TODO add your handling code here:
        
        try {
            //tao đối tượng Thí sinh mới lấy thông tin từ giao diện
            MonHoc ts = getThiSinhDetail();
            //thêm 1 bản ghi vào dsTS
//            monhoc.ThemMonHoc(ts);
            
            boolean tb= db.addmonhoc(ts);
            if(tb) {
                JOptionPane.showMessageDialog(rootPane, "thông báo thành công");
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "thông báo không thành công");
            }
            //vì dsTS đã thay đổi nên load lại bảng
            loadTable();
            //xóa các ô nhập liệu
            clearTextDetail();
        } catch (HeadlessException ex) {
            System.out.println("lỗi = " + ex.toString());
        }
    }//GEN-LAST:event_btnThemMouseClicked

